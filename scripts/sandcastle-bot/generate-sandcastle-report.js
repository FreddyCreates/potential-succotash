#!/usr/bin/env node
/**
 * 🏰 sandcastle-bot: generate-sandcastle-report.js
 *
 * Generates:
 *   - docs/sandcastle-report.md  — BTL pipeline report
 *   - docs/sandcastle-history.json — append-only history of land decisions
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DOCS = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Load decision + gates
let decision = { decision: 'UNKNOWN', reason: 'No decision file', gatesSummary: [] };
try {
  decision = JSON.parse(fs.readFileSync(path.join(DOCS, '_sandcastle-decision.json'), 'utf8'));
} catch {}

// Load gate details
const gateFiles = fs.readdirSync(DOCS).filter(f => f.startsWith('_sandcastle-') && f.endsWith('-gate.json'));
const gateDetails = [];
for (const gf of gateFiles) {
  try { gateDetails.push(JSON.parse(fs.readFileSync(path.join(DOCS, gf), 'utf8'))); } catch {}
}

const timestamp = new Date().toISOString();
const decisionIcon = decision.decision === 'PASS' ? '✅' : decision.decision === 'HOLD' ? '⚠️' : '🚫';

// ── Markdown report ───────────────────────────────────────────────────────────
const lines = [
  '# 🏰 organism-sandcastle-bot — BTL Pipeline Report',
  '',
  `**Generated:** ${timestamp}`,
  `**Land Decision:** ${decisionIcon} **${decision.decision}**`,
  `**Reason:** ${decision.reason}`,
  `**Risk Score:** ${(parseFloat(decision.riskScore || 0) * 100).toFixed(0)}%`,
  '',
  '## Gate Summary',
  '',
  '| Gate | Status | Checks Passed | Checks Failed |',
  '|---|---|---|---|',
  ...(decision.gatesSummary || []).map(g => {
    const icon = g.status === 'pass' ? '✅' : '❌';
    return `| **${g.gate.toUpperCase()}** | ${icon} ${g.status} | ${g.pass} | ${g.fail} |`;
  }),
  '',
];

// Per-gate details
for (const gate of gateDetails) {
  lines.push(`## ${gate.gate.charAt(0).toUpperCase() + gate.gate.slice(1)} Gate Details`);
  lines.push('');
  lines.push('| Check | Status | Detail |');
  lines.push('|---|---|---|');
  for (const c of gate.checks || []) {
    const icon = c.status === 'pass' ? '✅' : '❌';
    lines.push(`| ${c.name} | ${icon} ${c.status} | ${c.detail} |`);
  }
  lines.push('');
}

lines.push('---');
lines.push('');
lines.push('*Generated by organism-sandcastle-bot 🏰 · [History](./sandcastle-history.json)*');

fs.writeFileSync(path.join(DOCS, 'sandcastle-report.md'), lines.join('\n'));

// ── History (append-only) ─────────────────────────────────────────────────────
let history = [];
try {
  history = JSON.parse(fs.readFileSync(path.join(DOCS, 'sandcastle-history.json'), 'utf8'));
} catch {}

history.push({
  timestamp,
  decision: decision.decision,
  reason: decision.reason,
  riskScore: decision.riskScore,
  gates: (decision.gatesSummary || []).map(g => g.gate + ':' + g.status),
});

// Keep last 50 runs
if (history.length > 50) history = history.slice(-50);

fs.writeFileSync(path.join(DOCS, 'sandcastle-history.json'), JSON.stringify(history, null, 2));

console.log('');
console.log('🏰 organism-sandcastle-bot: Report Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Decision: ${decisionIcon} ${decision.decision}`);
console.log(`  Gates: ${(decision.gatesSummary || []).length} evaluated`);
console.log(`  History: ${history.length} runs recorded`);
console.log('  ✅ sandcastle-report.md written');
console.log('  ✅ sandcastle-history.json updated');
