#!/usr/bin/env node
/**
 * 🔄 deps-bot: generate-deps-report.js
 *
 * Generates:
 *   - docs/deps-report.md  — full dependency health dashboard
 *   - docs/deps-data.json  — machine-readable dep health state
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DOCS = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

let pkgDoc;
try {
  pkgDoc = JSON.parse(fs.readFileSync(path.join(DOCS, '_deps-packages.json'), 'utf8'));
} catch {
  console.error('❌ _deps-packages.json not found');
  process.exit(1);
}

const timestamp = new Date().toISOString();
const { stalenessSummary, auditTotal, packages, auditResults } = pkgDoc;

// Build deps-data.json
const depsData = {
  generated: timestamp,
  bot: 'organism-deps-bot',
  totalPackages: packages.length,
  totalDeps: pkgDoc.totalDeps,
  stalenessSummary,
  auditTotal,
  topRiskPackages: [...packages]
    .sort((a, b) => b.stalenessScore - a.stalenessScore)
    .slice(0, 5)
    .map(p => ({ name: p.name, score: p.stalenessScore.toFixed(3), criticalDeps: p.criticalDeps })),
};

fs.writeFileSync(path.join(DOCS, 'deps-data.json'), JSON.stringify(depsData, null, 2));

// ── Markdown dashboard ────────────────────────────────────────────────────────
const tierIcon = (t) => t === 'CRITICAL' ? '❌' : t === 'STALE' ? '🔶' : t === 'MONITOR' ? '⚠️' : '✅';
const overallStatus = (stalenessSummary?.critical || 0) === 0 ? '✅ HEALTHY' : '⚠️ ATTENTION NEEDED';

const lines = [
  '# 🔄 organism-deps-bot — Dependency Health Report',
  '',
  `**Generated:** ${timestamp}`,
  `**Status:** ${overallStatus}`,
  `**Packages:** ${packages.length} | **Total Deps:** ${pkgDoc.totalDeps}`,
  '',
  '## Staleness Summary',
  '',
  '| Tier | Count | Meaning |',
  '|---|---|---|',
  `| ✅ Fresh | ${stalenessSummary?.fresh || 0} | Exact pinned or tight patch range |`,
  `| ⚠️ Monitor | ${stalenessSummary?.monitor || 0} | Minor version drift allowed |`,
  `| 🔶 Stale | ${stalenessSummary?.stale || 0} | Major range or pre-release |`,
  `| ❌ Critical | ${stalenessSummary?.critical || 0} | Wildcard/git-ref/unpinned |`,
  '',
  '## Package Breakdown',
  '',
  '| Package | Deps | Critical | Staleness Score |',
  '|---|---|---|---|',
  ...packages.map(p =>
    `| **${p.name}** | ${p.totalDeps} | ${p.criticalDeps || 0} | ${(p.stalenessScore || 0).toFixed(3)} |`,
  ),
  '',
  '## Critical Dependencies',
  '',
  '| Dep | Version | Risk | Package |',
  '|---|---|---|---|',
  ...packages.flatMap(p =>
    p.deps
      .filter(d => d.analysis?.tier === 'CRITICAL')
      .map(d => `| \`${d.name}\` | \`${d.version}\` | ${d.analysis.risk} | ${p.name} |`)
  ).slice(0, 30),
  '',
  '## Security Audit',
  '',
  `**Total vulnerabilities found:** ${auditTotal || 0}`,
  '',
  '| Package | Audit Status |',
  '|---|---|',
  ...(auditResults || []).map(a =>
    `| **${a.package}** | ${a.status === 'clean' ? '✅ Clean' : a.status === 'not-installed' ? '— Not installed' : a.status === 'vulnerabilities-found' ? `❌ ${a.total} vulns` : '⚠️ ' + a.status} |`,
  ),
  '',
  '---',
  '',
  '*Generated by organism-deps-bot 🔄 · [Deps Data](./deps-data.json)*',
];

fs.writeFileSync(path.join(DOCS, 'deps-report.md'), lines.join('\n'));

console.log('');
console.log('🔄 organism-deps-bot: Report Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Packages: ${packages.length} | Deps: ${pkgDoc.totalDeps}`);
console.log(`  Critical: ${stalenessSummary?.critical || 0} | Stale: ${stalenessSummary?.stale || 0}`);
console.log('  ✅ deps-report.md written');
console.log('  ✅ deps-data.json written');
