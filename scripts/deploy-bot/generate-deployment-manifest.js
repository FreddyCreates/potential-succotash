#!/usr/bin/env node
/**
 * 🌐 deploy-bot: generate-deployment-manifest.js
 *
 * Generates:
 *   - docs/deployment-manifest.json  — full deployment state snapshot
 *   - docs/deployment-report.md      — human-readable deployment report
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT    = path.resolve(__dirname, '..', '..');
const DOCS    = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Read dfx.json
let dfx = {};
try { dfx = JSON.parse(fs.readFileSync(path.join(ROOT, 'dfx.json'), 'utf8')); } catch {}

// Read canister_ids.json if present
let canisters = {};
try { canisters = JSON.parse(fs.readFileSync(path.join(ROOT, 'canister_ids.json'), 'utf8')); } catch {}

// Substrate checks
const SUBSTRATES = ['typescript', 'python', 'cpp', 'java', 'motoko', 'web'];
const substrateStatus = {};
for (const sub of SUBSTRATES) {
  const subDir = path.join(ROOT, 'organism', sub);
  substrateStatus[sub] = {
    exists: fs.existsSync(subDir),
    fileCount: fs.existsSync(subDir) ?
      fs.readdirSync(subDir, { recursive: true }).filter(f => typeof f === 'string').length : 0,
  };
}

// Protocol count
const protoCount = fs.readdirSync(path.join(ROOT, 'protocols'))
  .filter(f => f.endsWith('.js') && f !== 'index.js' && f !== 'native-runtime.js').length;

// SDK count
const sdkCount = fs.readdirSync(path.join(ROOT, 'sdk'), { withFileTypes: true })
  .filter(d => d.isDirectory()).length;

// Extension count
const extCount = fs.readdirSync(path.join(ROOT, 'extensions'), { withFileTypes: true })
  .filter(d => d.isDirectory() && d.name !== 'windows').length;

const manifest = {
  generated: new Date().toISOString(),
  bot: 'organism-deploy-bot',
  commit: process.env.GITHUB_SHA || 'local',
  ref: process.env.GITHUB_REF || 'local',
  icp: {
    dfxVersion: dfx.dfx || 'unknown',
    canisters: dfx.canisters ? Object.keys(dfx.canisters) : [],
    networks: dfx.networks ? Object.keys(dfx.networks) : [],
    deployedCanisters: canisters,
  },
  organism: {
    substrates: substrateStatus,
    totalSubstrates: SUBSTRATES.filter(s => substrateStatus[s].exists).length,
  },
  system: {
    protocols: protoCount,
    sdks: sdkCount,
    extensions: extCount,
  },
};

fs.writeFileSync(path.join(DOCS, 'deployment-manifest.json'), JSON.stringify(manifest, null, 2));
console.log('✅ Deployment manifest written → docs/deployment-manifest.json');

// ── Report ────────────────────────────────────────────────────────────────────
const deployedCanisterNames = Object.keys(canisters);
const hasLiveDeployment = deployedCanisterNames.length > 0;

const report = [
  '# 🌐 organism-deploy-bot — Deployment Report',
  '',
  `**Generated:** ${manifest.generated}`,
  `**Commit:** \`${manifest.commit.slice(0, 7)}\``,
  `**Ref:** \`${manifest.ref}\``,
  '',
  '## ICP Configuration',
  '',
  `| Field | Value |`,
  `|---|---|`,
  `| DFX Version | \`${manifest.icp.dfxVersion}\` |`,
  `| Defined Canisters | ${manifest.icp.canisters.join(', ')} |`,
  `| Networks | ${manifest.icp.networks.join(', ')} |`,
  `| Live Canisters | ${hasLiveDeployment ? deployedCanisterNames.join(', ') : 'None deployed yet'} |`,
  '',
  '## Organism Substrates',
  '',
  `| Substrate | Status | Files |`,
  `|---|---|---|`,
  ...SUBSTRATES.map(s => {
    const status = substrateStatus[s];
    return `| **${s}** | ${status.exists ? '✅ Present' : '❌ Missing'} | ${status.fileCount} |`;
  }),
  '',
  '## System Stats',
  '',
  `| Layer | Count |`,
  `|---|---|`,
  `| Protocols | ${protoCount} |`,
  `| SDKs | ${sdkCount} |`,
  `| Extensions | ${extCount} |`,
  `| Substrates Active | ${manifest.organism.totalSubstrates}/6 |`,
  '',
  hasLiveDeployment ? [
    '## Live Canisters',
    '',
    ...deployedCanisterNames.map(name => {
      const id = canisters[name]?.ic || '—';
      return `- **${name}**: \`${id}\` → [https://${id}.icp0.io](https://${id}.icp0.io)`;
    }),
    '',
  ].join('\n') : '## Live Deployment\n\nNo canisters deployed yet. Run the deploy workflow with `DFX_IDENTITY_PEM` secret to deploy.\n',
  '---',
  '',
  '*Generated by organism-deploy-bot 🌐*',
].join('\n');

fs.writeFileSync(path.join(DOCS, 'deployment-report.md'), report);
console.log('✅ Deployment report written → docs/deployment-report.md');
