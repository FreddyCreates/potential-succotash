#!/usr/bin/env node
/**
 * 🕷️ crawler-bot: generate-organism-map.js
 *
 * Generates:
 *   - docs/organism-map.md  — full living organism topology map
 *   - docs/topology.json    — machine-readable topology snapshot
 *
 * Reads from docs/_topology-raw.json and docs/_cross-reference.json.
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DOCS = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Load topology and cross-reference
let topoDoc  = null;
let xrefDoc  = null;
try { topoDoc  = JSON.parse(fs.readFileSync(path.join(DOCS, '_topology-raw.json'), 'utf8')); } catch {}
try { xrefDoc  = JSON.parse(fs.readFileSync(path.join(DOCS, '_cross-reference.json'), 'utf8')); } catch {}

const timestamp = new Date().toISOString();

// ── Build topology.json ────────────────────────────────────────────────────────
const topology = {
  generated: timestamp,
  bot: 'organism-crawler-bot',
  summary: topoDoc?.summary || {},
  sections: topoDoc?.sections || [],
  crossRef: xrefDoc?.summary || {},
  topSymbols: Object.entries(xrefDoc?.symbolIndex || {})
    .sort(([, a], [, b]) => b.length - a.length)
    .slice(0, 20)
    .map(([name, locs]) => ({ name, definedIn: locs.length, locations: locs.slice(0, 3) })),
};

fs.writeFileSync(path.join(DOCS, 'topology.json'), JSON.stringify(topology, null, 2));

// ── Markdown organism map ─────────────────────────────────────────────────────
const formatBytes = (b) => b < 1024 ? b + ' B' : b < 1048576 ? (b / 1024).toFixed(1) + ' KB' : (b / 1048576).toFixed(2) + ' MB';

const langBar = (counts) => {
  const total = Object.values(counts).reduce((a, c) => a + c, 0);
  return Object.entries(counts)
    .sort(([, a], [, b]) => b - a)
    .slice(0, 4)
    .map(([l, c]) => `${l}: ${Math.round(c / total * 100)}%`)
    .join(' · ');
};

const sections = topoDoc?.sections || [];
const summary  = topoDoc?.summary || {};
const xSummary = xrefDoc?.summary || {};

const lines = [
  '# 🕷️ organism-crawler-bot — Organism Map',
  '',
  `**Generated:** ${timestamp}`,
  `**Bot:** organism-crawler-bot 🕷️`,
  '',
  '## Repository Overview',
  '',
  `| Metric | Value |`,
  `|---|---|`,
  `| Total Files | ${summary.totalFiles || '—'} |`,
  `| Total Size | ${formatBytes(summary.totalBytes || 0)} |`,
  `| Top-Level Sections | ${sections.length} |`,
  `| Analyzed Files (xref) | ${xSummary.totalFiles || '—'} |`,
  `| Total Imports | ${xSummary.totalImports || '—'} |`,
  `| Unique Symbols | ${xSummary.uniqueSymbols || '—'} |`,
  '',
  '## Directory Topology',
  '',
  '| Section | Files | Size | Top Language |',
  '|---|---|---|---|',
  ...sections
    .sort((a, b) => b.files - a.files)
    .map(s => `| **${s.name}/** | ${s.files} | ${formatBytes(s.bytes)} | ${s.topLanguage} |`),
  '',
  '## Language Breakdown',
  '',
  '| Language | Files | Size |',
  '|---|---|---|',
  ...Object.entries(summary.langCounts || {})
    .sort(([, a], [, b]) => b - a)
    .map(([lang, count]) => `| **${lang}** | ${count} | ${formatBytes(summary.langBytes?.[lang] || 0)} |`),
  '',
  '## Cross-Reference Summary',
  '',
  ...Object.entries(xrefDoc?.crossRef || {}).map(([section, files]) => {
    const totalImports = files.reduce((a, f) => a + f.imports.length, 0);
    const totalExports = files.reduce((a, f) => a + f.exports.length, 0);
    const classes   = files.reduce((a, f) => a + f.exports.filter(e => e.kind === 'class').length, 0);
    const functions = files.reduce((a, f) => a + f.exports.filter(e => e.kind === 'function').length, 0);
    return [
      `### \`${section}/\``,
      '',
      `- **Files analyzed:** ${files.length}`,
      `- **Imports:** ${totalImports}`,
      `- **Exports:** ${totalExports} (${classes} classes, ${functions} functions)`,
      '',
    ].join('\n');
  }),
  '## Top Exported Symbols',
  '',
  '*(Most referenced across the codebase)*',
  '',
  '| Symbol | Kind | Defined In | Locations |',
  '|---|---|---|---|',
  ...topology.topSymbols.slice(0, 15).map(s =>
    `| \`${s.name}\` | ${s.locations[0]?.kind || '—'} | ${s.definedIn} file(s) | ${s.locations.map(l => `\`${l.file}\``).join(', ')} |`,
  ),
  '',
  '## Section Detail',
  '',
  ...sections.sort((a, b) => b.files - a.files).map(s => [
    `### \`${s.name}/\``,
    '',
    `- **Files:** ${s.files}`,
    `- **Subdirectories:** ${s.dirs}`,
    `- **Size:** ${formatBytes(s.bytes)}`,
    `- **Primary Language:** ${s.topLanguage}`,
    '',
  ].join('\n')),
  '---',
  '',
  '*Generated by organism-crawler-bot 🕷️ · [Topology JSON](./topology.json)*',
];

fs.writeFileSync(path.join(DOCS, 'organism-map.md'), lines.join('\n'));

console.log('');
console.log('🕷️ organism-crawler-bot: Organism Map Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Files mapped:     ${summary.totalFiles || '—'}`);
console.log(`  Total size:       ${formatBytes(summary.totalBytes || 0)}`);
console.log(`  Unique symbols:   ${xSummary.uniqueSymbols || '—'}`);
console.log('  ✅ organism-map.md written');
console.log('  ✅ topology.json written');
