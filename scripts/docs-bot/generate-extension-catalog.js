#!/usr/bin/env node
/**
 * 📚 docs-bot: generate-extension-catalog.js
 *
 * Generates docs/extension-catalog.md from:
 *   - extensions/index.js  (the EXTENSIONS wire array)
 *   - Each extension's manifest.json (for live version/description)
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT    = path.resolve(__dirname, '..', '..');
const EXT_DIR = path.resolve(ROOT, 'extensions');
const DOCS    = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Load wire index
let wireExtensions = [];
try {
  // Parse EXTENSIONS array from index.js without executing it
  const src = fs.readFileSync(path.join(EXT_DIR, 'index.js'), 'utf8');
  // Extract each {id, slug, name, ...} block
  const entryRe = /\{\s*id:\s*'(EXT-\d+)',\s*slug:\s*'([^']+)',\s*name:\s*'([^']+)'[^}]*ring:\s*'([^']+)'[^}]*\}/g;
  let m;
  while ((m = entryRe.exec(src)) !== null) {
    wireExtensions.push({ id: m[1], slug: m[2], name: m[3], ring: m[4] });
  }
} catch (e) {
  console.warn('⚠ Could not parse extensions/index.js:', e.message);
}

// Scan all extension directories for manifests
const extDirs = fs.readdirSync(EXT_DIR, { withFileTypes: true })
  .filter(d => d.isDirectory() && d.name !== 'windows')
  .map(d => d.name)
  .sort();

const catalog = [];

for (const dir of extDirs) {
  const manifestPath = path.join(EXT_DIR, dir, 'manifest.json');
  if (!fs.existsSync(manifestPath)) continue;

  let manifest = {};
  try { manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8')); } catch {}

  // Match to wire index
  const wire = wireExtensions.find(e => e.slug === dir);

  catalog.push({
    id:          wire?.id || '—',
    slug:        dir,
    name:        manifest.name || wire?.name || dir,
    version:     manifest.version || '—',
    description: manifest.description || '',
    ring:        wire?.ring || '—',
    permissions: manifest.permissions || [],
    hasBackground: !!(manifest.background?.service_worker),
    hasContentScript: !!(manifest.content_scripts?.length),
  });
}

// ── Markdown catalog ──────────────────────────────────────────────────────────
const timestamp = new Date().toISOString();

const lines = [
  '# 🧬 Extension Catalog',
  '',
  `**Generated:** ${timestamp}`,
  `**Total Extensions:** ${catalog.length}`,
  `**Bot:** organism-docs-bot 📚`,
  '',
  '## Extension Registry',
  '',
  '| ID | Name | Version | Ring | Background | Content Script |',
  '|---|---|---|---|---|---|',
  ...catalog.map(e => [
    `| ${e.id}`,
    ` | **${e.name}** |`,
    ` \`${e.version}\` |`,
    ` ${e.ring} |`,
    ` ${e.hasBackground ? '✅' : '—'} |`,
    ` ${e.hasContentScript ? '✅' : '—'} |`,
  ].join('')),
  '',
  '## Extension Details',
  '',
  ...catalog.map(e => [
    `### ${e.id} — ${e.name}`,
    '',
    `- **Slug:** \`${e.slug}\``,
    `- **Version:** \`${e.version}\``,
    `- **Ring:** ${e.ring}`,
    e.description ? `- **Description:** ${e.description}` : '',
    e.permissions.length ? `- **Permissions:** ${e.permissions.slice(0, 6).join(', ')}${e.permissions.length > 6 ? ` (+${e.permissions.length - 6} more)` : ''}` : '',
    `- **Background Worker:** ${e.hasBackground ? '✅' : 'None'}`,
    `- **Content Script:** ${e.hasContentScript ? '✅' : 'None'}`,
    '',
  ].filter(Boolean).join('\n')),
  '---',
  '',
  '*Generated by organism-docs-bot 📚*',
];

fs.writeFileSync(path.join(DOCS, 'extension-catalog.md'), lines.join('\n'));
console.log(`✅ Extension catalog: ${catalog.length} extensions → docs/extension-catalog.md`);
