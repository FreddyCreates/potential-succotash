#!/usr/bin/env node
/**
 * 📸 visual-bot: generate-visual-report.js
 *
 * Generates docs/visual-report.md from the visual diff results.
 * Shows per-surface status, baseline delta, and overall visual health.
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DOCS = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Load diff
let diffDoc;
try {
  diffDoc = JSON.parse(fs.readFileSync(path.join(DOCS, '_visual-diff.json'), 'utf8'));
} catch {
  console.error('❌ _visual-diff.json not found — run compare-baselines.js first');
  process.exit(1);
}

const { summary, diffs } = diffDoc;
const timestamp = new Date().toISOString();

const totalSurfaces = diffs.length;
const overallStatus = summary.regression === 0
  ? '✅ ALL SURFACES PASS'
  : `❌ ${summary.regression} REGRESSION(S) DETECTED`;

const byType = {};
for (const d of diffs) {
  const t = d.type || 'unknown';
  if (!byType[t]) byType[t] = [];
  byType[t].push(d);
}

const statusIcon = (s) => s === 'pass' ? '✅' : s === 'new' ? '🆕' : s === 'changed' ? '🔶' : '❌';

const lines = [
  '# 📸 organism-visual-bot — Visual Regression Report',
  '',
  `**Generated:** ${timestamp}`,
  `**Status:** ${overallStatus}`,
  `**Total Surfaces:** ${totalSurfaces}`,
  '',
  '## Summary',
  '',
  '| Status | Count |',
  '|---|---|',
  `| 🆕 New (first baseline) | ${summary.new} |`,
  `| ✅ Pass (no change) | ${summary.pass} |`,
  `| 🔶 Changed (content evolved) | ${summary.changed} |`,
  `| ❌ Regression (pixel diff > threshold) | ${summary.regression} |`,
  '',
  '## Surfaces',
  '',
];

for (const [type, surfaces] of Object.entries(byType)) {
  lines.push(`### ${type.charAt(0).toUpperCase() + type.slice(1)} surfaces`);
  lines.push('');
  lines.push('| Surface | Status | Detail |');
  lines.push('|---|---|---|');
  for (const s of surfaces) {
    lines.push(`| \`${s.relPath || s.id}\` | ${statusIcon(s.status)} ${s.status} | ${s.detail || '—'} |`);
  }
  lines.push('');
}

lines.push('---');
lines.push('');
lines.push('*Generated by organism-visual-bot 📸 · Baselines: [visual-baselines/](./visual-baselines/)*');

fs.writeFileSync(path.join(DOCS, 'visual-report.md'), lines.join('\n'));

console.log('');
console.log('📸 organism-visual-bot: Visual Report Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Total: ${totalSurfaces} | New: ${summary.new} | Pass: ${summary.pass} | Changed: ${summary.changed}`);
console.log(`  Status: ${overallStatus}`);
console.log('  ✅ visual-report.md written');
