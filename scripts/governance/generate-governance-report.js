#!/usr/bin/env node
/**
 * 🌐 governance: generate-governance-report.js
 *
 * Step 7 of the Atlas governance cycle.
 * Generates the human-readable governance narrative.
 *
 * Produces:
 *   - docs/governance-cycle.json        — machine-readable cycle result
 *   - docs/governance-report.md         — human-readable narrative
 *   - docs/governance-history.json      — append-only history (last 100 cycles)
 *   - dist/governance/events/evt-governance-<ts>.json — governance_cycle_completed event
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const { emitEvent } = require('./emit-event');

const ROOT    = path.resolve(__dirname, '..', '..');
const GOV_MEM = path.join(ROOT, 'governance', 'memory');
const DOCS    = path.join(ROOT, 'docs');

fs.mkdirSync(DOCS,    { recursive: true });
fs.mkdirSync(GOV_MEM, { recursive: true });

function loadJson(file, def) {
  try { return JSON.parse(fs.readFileSync(path.join(GOV_MEM, file), 'utf8')); } catch { return def; }
}
function loadDocsJson(file, def) {
  try { return JSON.parse(fs.readFileSync(path.join(DOCS, file), 'utf8')); } catch { return def; }
}

const manifest    = loadJson('_ingested-events.json', { eventCount: 0, events: [] });
const decisionDoc = loadJson('_law-decisions.json',   { totalFired: 0, riskScore: '0', activeForbids: [], warnings: [], escalations: [] });
const pipelineDoc = loadJson('_pipeline-results.json',{ domainResults: {}, overallRisk: 0, overallDecision: 'ALLOW' });

const timestamp = new Date().toISOString();

// ── Decide overall status ─────────────────────────────────────────────────────
const PHI            = 1.618033988749895;
const overallRisk    = pipelineDoc.overallRisk || 0;
const overallDecision = pipelineDoc.overallDecision || 'ALLOW';

const statusIcon = overallDecision === 'ALLOW'          ? '✅'
  : overallDecision === 'WARN'                          ? '⚠️'
  : overallDecision === 'ESCALATE'                      ? '🔺'
  : '🚫';

const overallStatus = `${statusIcon} ${overallDecision}`;

// ── Markdown report ───────────────────────────────────────────────────────────
const domainRows = Object.entries(pipelineDoc.domainResults || {}).map(([domain, r]) => {
  const icon = r.status === 'pass' ? '✅' : r.status === 'warned' ? '⚠️' : '❌';
  return `| **${domain}** | ${r.pipelineId.replace('pipeline://governance/', '')} | ${icon} ${r.status} | ${(r.riskScore * 100).toFixed(0)}% | ${r.branchDecision} |`;
});

const forbidRows = (decisionDoc.activeForbids || []).slice(0, 10).map(d =>
  `| \`${d.lawSetId}.${d.ruleId}\` | \`${d.target}\` | \`${d.entity}\` |`,
);

const warnRows = (decisionDoc.warnings || []).slice(0, 10).map(d =>
  `| \`${d.lawSetId}.${d.ruleId}\` | \`${d.target}\` | \`${d.entity}\` |`,
);

const lines = [
  '# 🌐 Atlas Governance Cycle Report',
  '',
  `**Generated:** ${timestamp}`,
  `**Decision:** ${overallStatus}`,
  `**Risk Score:** ${(overallRisk * 100).toFixed(1)}%  _(block threshold: ${(1/(PHI*PHI)*100).toFixed(0)}%  |  escalate: ${(1/PHI*100).toFixed(0)}%)_`,
  `**Events Processed:** ${manifest.eventCount}`,
  `**Laws Fired:** ${decisionDoc.totalFired}`,
  '',
  '## Domain Pipeline Results',
  '',
  '| Domain | Pipeline | Status | Risk | Decision |',
  '|---|---|---|---|---|',
  ...domainRows,
  '',
  ...(forbidRows.length > 0 ? [
    '## Active Blocks (FORBID)',
    '',
    '| Law.Rule | Target | Entity |',
    '|---|---|---|',
    ...forbidRows,
    '',
  ] : []),
  ...(warnRows.length > 0 ? [
    '## Warnings',
    '',
    '| Law.Rule | Target | Entity |',
    '|---|---|---|',
    ...warnRows,
    '',
  ] : []),
  '## Risk Legend',
  '',
  '| Zone | Risk | Meaning |',
  '|---|---|---|',
  `| ✅ Safe | 0% – ${(1/(PHI*PHI)*100).toFixed(0)}% | All clear |`,
  `| ⚠️ Monitor | ${(1/(PHI*PHI)*100).toFixed(0)}% – ${(1/PHI*100).toFixed(0)}% | Watch but proceed |`,
  `| 🔺 Escalate | ${(1/PHI*100).toFixed(0)}% – 90% | Human review required |`,
  `| 🚫 Block | 90%+ | Hard stop |`,
  '',
  '---',
  '',
  `*Generated by organism-alpha-bot 👑 · Atlas Governance v1.0.0 · φ=${PHI}*`,
];

fs.writeFileSync(path.join(DOCS, 'governance-report.md'), lines.join('\n'));

// ── Machine-readable cycle result ─────────────────────────────────────────────
const cycleResult = {
  id:             `cycle-${timestamp}`,
  generated:      timestamp,
  decision:       overallDecision,
  riskScore:      overallRisk,
  eventCount:     manifest.eventCount,
  lawsFired:      decisionDoc.totalFired,
  forbids:        decisionDoc.activeForbids?.length || 0,
  warnings:       decisionDoc.warnings?.length || 0,
  escalations:    decisionDoc.escalations?.length || 0,
  domainResults:  pipelineDoc.domainResults,
};

fs.writeFileSync(path.join(DOCS, 'governance-cycle.json'), JSON.stringify(cycleResult, null, 2));

// ── Governance history ────────────────────────────────────────────────────────
let history = loadDocsJson('governance-history.json', []);
history.push({
  ts:       timestamp,
  decision: overallDecision,
  risk:     overallRisk,
  events:   manifest.eventCount,
  forbids:  decisionDoc.activeForbids?.length || 0,
});
if (history.length > 100) history = history.slice(-100);
fs.writeFileSync(path.join(DOCS, 'governance-history.json'), JSON.stringify(history, null, 2));

// ── Emit governance_cycle_completed event ─────────────────────────────────────
emitEvent(
  'atlas://bot/organism-alpha-bot',
  'governance_cycle_completed',
  {
    status:       overallDecision === 'ALLOW' ? 'pass' : overallDecision === 'WARN' ? 'warn' : 'blocked',
    risk_score:   overallRisk,
    health_dashboard: {
      overall:  overallRisk <= 0.382 ? 'green' : overallRisk <= 0.618 ? 'yellow' : 'red',
    },
    metrics: {
      eventCount:  manifest.eventCount,
      lawsFired:   decisionDoc.totalFired,
      forbids:     decisionDoc.activeForbids?.length || 0,
      warnings:    decisionDoc.warnings?.length || 0,
    },
  },
  ['bot', 'alpha', 'governance', 'division-vii'],
  { id: `evt-gov-cycle-${timestamp.replace(/[:.]/g, '-')}` },
);

console.log('');
console.log('🌐 Atlas Governance: Report Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Decision:  ${overallStatus}`);
console.log(`  Risk:      ${(overallRisk * 100).toFixed(1)}%`);
console.log(`  Events:    ${manifest.eventCount}`);
console.log(`  History:   ${history.length} cycles`);
console.log('  ✅ governance-report.md written');
console.log('  ✅ governance-cycle.json written');
console.log('  ✅ governance-history.json updated');
