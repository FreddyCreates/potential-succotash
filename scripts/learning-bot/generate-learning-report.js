#!/usr/bin/env node
/**
 * 🎓 learning-bot: generate-learning-report.js
 *
 * Generates docs/learning-report.md from the learning state.
 * Shows:
 *   - Training epoch and signal summary
 *   - Top Hebbian synapse pairs
 *   - Protocol weight ranking
 *   - Learning trajectory (improving / stable / regressing)
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT      = path.resolve(__dirname, '..', '..');
const DOCS      = path.resolve(ROOT, 'docs');
const LEARN_DIR = path.resolve(ROOT, 'dist', 'learning');

fs.mkdirSync(DOCS, { recursive: true });
fs.mkdirSync(LEARN_DIR, { recursive: true });

// Load state and signals
let state = { synapses: {}, protocolWeights: {}, epoch: 0, lastTrained: null };
try {
  state = JSON.parse(fs.readFileSync(path.join(LEARN_DIR, 'learning-state.json'), 'utf8'));
} catch {}

let sigDoc = { signals: [], totalSignals: 0, gathered: null };
try {
  sigDoc = JSON.parse(fs.readFileSync(path.join(DOCS, '_learning-signals.json'), 'utf8'));
} catch {}

const timestamp = new Date().toISOString();
const PHI = 1.618033988749895;

// ── Top synapses ──────────────────────────────────────────────────────────────
const synapseList = [];
for (const [from, targets] of Object.entries(state.synapses || {})) {
  for (const [to, weight] of Object.entries(targets)) {
    if (from < to) { // avoid duplicates (symmetric)
      synapseList.push({ from, to, weight });
    }
  }
}
synapseList.sort((a, b) => b.weight - a.weight);
const totalSynapses = synapseList.length;

// ── Protocol weight ranking ───────────────────────────────────────────────────
const weightRanking = Object.entries(state.protocolWeights || {})
  .sort(([, a], [, b]) => b - a);

// ── Signal breakdown ──────────────────────────────────────────────────────────
const signalTypes = {};
for (const s of sigDoc.signals) {
  signalTypes[s.type] = (signalTypes[s.type] || 0) + 1;
}
const avgStrength = sigDoc.signals.length > 0
  ? sigDoc.signals.reduce((a, s) => a + s.strength, 0) / sigDoc.signals.length : 0;

// ── Trend ─────────────────────────────────────────────────────────────────────
// Phi-emergence threshold
const emergence = totalSynapses > 0
  ? Math.min(1.0, (totalSynapses / (sigDoc.totalSignals || 1)) * PHI)
  : 0;
const emergenceLevel = emergence > 0.618 ? '🌟 EMERGED' : emergence > 0.3 ? '📈 Growing' : '🌱 Early';

// ── Markdown report ───────────────────────────────────────────────────────────
const bar = (v) => '█'.repeat(Math.round(Math.min(1, v) * 10)) + '░'.repeat(10 - Math.round(Math.min(1, v) * 10));

const lines = [
  '# 🎓 organism-learning-bot — Learning Report',
  '',
  `**Generated:** ${timestamp}`,
  `**Training Epoch:** ${state.epoch}`,
  `**Last Trained:** ${state.lastTrained || '—'}`,
  `**Emergence Level:** ${emergenceLevel} (${(emergence * 100).toFixed(1)}%)`,
  '',
  '## Training Summary',
  '',
  `| Metric | Value |`,
  `|---|---|`,
  `| Total Signals | ${sigDoc.totalSignals} |`,
  `| Average Signal Strength | ${avgStrength.toFixed(3)} |`,
  `| Hebbian Synapses | ${totalSynapses} |`,
  `| Protocol Weights | ${weightRanking.length} |`,
  `| Phi-Emergence | ${(emergence * 100).toFixed(1)}% |`,
  '',
  '## Signal Breakdown',
  '',
  `| Signal Type | Count |`,
  `|---|---|`,
  ...Object.entries(signalTypes).sort(([, a], [, b]) => b - a).map(([t, c]) => `| \`${t}\` | ${c} |`),
  '',
  '## Protocol Weight Ranking',
  '',
  '*(TD learning with γ = 1/φ ≈ 0.618)*',
  '',
  `| Rank | Protocol | Weight | Visual |`,
  `|---|---|---|---|`,
  ...weightRanking.slice(0, 10).map(([p, w], i) =>
    `| ${i + 1} | \`${p}\` | ${w.toFixed(3)} | \`${bar(w)}\` |`,
  ),
  '',
  '## Strongest Hebbian Synapses',
  '',
  '*(Modules that consistently co-succeed)*',
  '',
  `| Module A | Module B | Weight |`,
  `|---|---|---|`,
  ...synapseList.slice(0, 10).map(s =>
    `| \`${s.from.slice(0, 35)}\` | \`${s.to.slice(0, 35)}\` | ${s.weight.toFixed(4)} |`,
  ),
  totalSynapses === 0 ? '| — | — | No synapses yet |' : '',
  '',
  '## Learning Constants',
  '',
  `| Constant | Value | Meaning |`,
  `|---|---|---|`,
  `| PHI | ${PHI} | Golden ratio |`,
  `| γ (discount) | ${(1.0 / PHI).toFixed(4)} | 1/φ — phi-based TD discount |`,
  `| α (Hebb rate) | 0.10 | Hebbian learning rate |`,
  `| α (TD rate) | 0.05 | TD learning rate |`,
  `| λ (decay) | ${(1.0 / (PHI * PHI * 10)).toFixed(5)} | Phi-modulated synapse decay |`,
  `| Threshold | 0.618 | Phi-emergence threshold |`,
  '',
  '---',
  '',
  '*Generated by organism-learning-bot 🎓 · State: [learning-state.json](../dist/learning/learning-state.json)*',
];

fs.writeFileSync(path.join(DOCS, 'learning-report.md'), lines.join('\n'));

// Ensure initial learning state exists for git commit
if (!fs.existsSync(path.join(LEARN_DIR, 'learning-state.json'))) {
  fs.writeFileSync(path.join(LEARN_DIR, 'learning-state.json'), JSON.stringify(state, null, 2));
}

console.log('');
console.log('🎓 organism-learning-bot: Learning Report');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Epoch:        ${state.epoch}`);
console.log(`  Signals:      ${sigDoc.totalSignals}`);
console.log(`  Synapses:     ${totalSynapses}`);
console.log(`  Protocols:    ${weightRanking.length}`);
console.log(`  Emergence:    ${emergenceLevel}`);
console.log('  ✅ learning-report.md written');
