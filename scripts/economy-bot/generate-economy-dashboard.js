#!/usr/bin/env node
/**
 * 💰 economy-bot: generate-economy-dashboard.js
 *
 * Generates:
 *   - docs/economy-dashboard.md  — full economy dashboard
 *   - docs/economy-data.json     — machine-readable economy state
 *
 * Reads from docs/_economy-assets.json.
 */

'use strict';

const fs   = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const DOCS = path.resolve(ROOT, 'docs');

fs.mkdirSync(DOCS, { recursive: true });

// Load assets
let assetDoc;
try {
  assetDoc = JSON.parse(fs.readFileSync(path.join(DOCS, '_economy-assets.json'), 'utf8'));
} catch {
  console.error('❌ _economy-assets.json not found — run scan-assets.js first');
  process.exit(1);
}

const { assets, summary, coverage } = assetDoc;
const timestamp = new Date().toISOString();

// ── Economy data JSON ─────────────────────────────────────────────────────────
const economyData = {
  generated: timestamp,
  bot: 'organism-economy-bot',
  summary,
  coverage,
  topAssets: {
    highestComplexitySDK: assets.sdks.sort((a, b) => b.exportCount - a.exportCount)[0]?.name,
    largestProtocol: assets.protocols.sort((a, b) => b.lineCount - a.lineCount)[0]?.className,
    mostMethodsProtocol: assets.protocols.sort((a, b) => b.methodCount - a.methodCount)[0]?.className,
    largestCSV: assets.csvRegisters.sort((a, b) => b.rowCount - a.rowCount)[0]?.name,
  },
};

fs.writeFileSync(path.join(DOCS, 'economy-data.json'), JSON.stringify(economyData, null, 2));

// ── Markdown dashboard ────────────────────────────────────────────────────────
const coverageBar = (pct) => {
  const filled = Math.round(pct / 10);
  return '█'.repeat(filled) + '░'.repeat(10 - filled) + ` ${pct}%`;
};

const lines = [
  '# 💰 organism-economy-bot — Economy Dashboard',
  '',
  `**Generated:** ${timestamp}`,
  `**Bot:** organism-economy-bot 💰`,
  '',
  '## Asset Inventory',
  '',
  '| Asset Type | Count | Complexity Weight | Total Score |',
  '|---|---|---|---|',
  `| 🧬 Extensions | ${assets.extensions.length} | 2–4 pts | ${assets.extensions.reduce((a, e) => a + e.complexity, 0)} |`,
  `| 📦 SDKs | ${assets.sdks.length} | 5 pts | ${assets.sdks.length * 5} |`,
  `| 🔬 Protocols | ${assets.protocols.length} | 3 pts | ${assets.protocols.length * 3} |`,
  `| 📊 CSV Registers | ${assets.csvRegisters.length} | 1 pt | ${assets.csvRegisters.length} |`,
  `| **Total** | **${summary.total}** | — | **${summary.totalComplexity}** |`,
  '',
  '## Coverage Analysis',
  '',
  `| Dimension | Coverage | Visual |`,
  `|---|---|---|`,
  `| SDK Wiring | ${coverage.sdkCoverage}% | \`${coverageBar(coverage.sdkCoverage)}\` |`,
  `| Protocol Wiring | ${coverage.protocolCoverage}% | \`${coverageBar(coverage.protocolCoverage)}\` |`,
  `| Register Presence | ${coverage.registerCoverage}% | \`${coverageBar(coverage.registerCoverage)}\` |`,
  `| **Overall** | **${coverage.overallCoverage}%** | \`${coverageBar(coverage.overallCoverage)}\` |`,
  '',
  '## Extensions',
  '',
  '| Extension | Version | Type | Complexity |',
  '|---|---|---|---|',
  ...assets.extensions.map(e =>
    `| **${e.name}** | \`${e.version}\` | ${e.hasVite ? 'Vite/React' : 'Simple MV3'} | ${e.complexity} |`,
  ),
  '',
  '## SDKs',
  '',
  '| SDK | Version | Exports | Complexity |',
  '|---|---|---|---|',
  ...assets.sdks.map(s =>
    `| **${s.name}** | \`${s.version}\` | ${s.exportCount} | ${s.complexity} |`,
  ),
  '',
  '## Protocols',
  '',
  '| Protocol | Class | Methods | Lines | PHI | Heartbeat |',
  '|---|---|---|---|---|---|',
  ...assets.protocols.map(p =>
    `| \`${p.slug}\` | \`${p.className}\` | ${p.methodCount} | ${p.lineCount} | ${p.hasPHI ? '✓' : '—'} | ${p.hasHeartbeat ? '✓' : '—'} |`,
  ),
  '',
  '## CSV Registers',
  '',
  '| Register | Rows | Columns |',
  '|---|---|---|',
  ...assets.csvRegisters.map(c => `| **${c.name}** | ${c.rowCount} | ${c.columns} |`),
  '',
  '---',
  '',
  '*Generated by organism-economy-bot 💰 · [Economy Data](./economy-data.json)*',
];

fs.writeFileSync(path.join(DOCS, 'economy-dashboard.md'), lines.join('\n'));
console.log('');
console.log('💰 organism-economy-bot: Dashboard Generated');
console.log('══════════════════════════════════════════════════════════');
console.log(`  Total assets: ${summary.total} (complexity: ${summary.totalComplexity})`);
console.log(`  Overall coverage: ${coverage.overallCoverage}%`);
console.log('  ✅ economy-dashboard.md written');
console.log('  ✅ economy-data.json written');
